    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
                                                
    String tipoSeleccionado = cmbTipoTransporte.getSelectedItem().toString();

    switch (tipoSeleccionado) {
        case "Furboneta":
            TxtTipoTransporte.setText("Furgoneta negra - AAA 0589 - Conductor: Alejandro Magno\n"
                    + "Furgoneta Gris - AAA 0090 - Conductor: Fernando Arias");
            break;
        case "Taxi":
            TxtTipoTransporte.setText("0987772389 - Taxi Miraflores\n"
                    + "0987845555 - Taxi Viracochabamba\n"
                    + "0989882999 - Taxi Baños\n"
                    + "0999929292 - Taxi Panamericana\n"
                    + "0999292921 - Taxi Pumapungo");
            break;
        case "Camioneta":
            TxtTipoTransporte.setText("0987772389 - Camioneta Miraflores\n"
                    + "0987845555 - Camioneta Viracochabamba\n"
                    + "0989882999 - Camioneta Baños\n"
                    + "0999929292 - Camioneta Panamericana\n"
                    + "0999292921 - Camioneta Pumapungo");
            break;
        case "bus":
            TxtTipoTransporte.setText("Bus interprovincial Sucre: Hora de salida: 12:00, 14:00, 16:00, 19:00\n"
                    + "Bus Internacional Loja: Hora de salida: 07:00, 09:00, 12:00, 14:00, 17:00, 21:00\n"
                    + "Bus Santa: Hora de salida: 12:00, 14:00, 16:00, 19:00");
            break;
        default:
            TxtTipoTransporte.setText("Por favor selecciona un tipo de transporte.");
    }
    }//GEN-LAST:event_btnBuscarActionPerformed

